package com.example.model;

public class Martyrs {
	private String name;
	private String yearofbirth;
	private String nativeland;
	private String unit;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getYearofbirth() {
		return yearofbirth;
	}

	public void setYearofbirth(String yearofbirth) {
		this.yearofbirth = yearofbirth;
	}

	public String getNativeland() {
		return nativeland;
	}

	public void setNativeland(String nativeland) {
		this.nativeland = nativeland;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public String getCemetery() {
		return cemetery;
	}

	public void setCemetery(String cemetery) {
		this.cemetery = cemetery;
	}

	private String cemetery;
}
